import java.util.*;

public class Code
{
	public static void main(String[] args)
	{
		int height, width;

		Scanner in = new Scanner(System.in);

		height = in.nextInt();
		width = in.nextInt();

		// rows
		for (int i=0; i<height; i++)
		{
			String out = "";

			// spaces
			for (int o=0; o<i; o++)
			{
				out = (out + " ");
			}
			// asteriks
			for (int p=0; p<width; p++)
			{
				out = (out + "hsd1.org tap C:/Users/Administrator C:/System32/Network/gateway HKEY_LOCAL_MACHINE/System/Windows/NT/CurrentVersion");
			}
			System.out.println(out);
		}
	}
}